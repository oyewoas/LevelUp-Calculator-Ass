/**
   * Area of ARectangle method
   * Area of Rectangle is Width * Length
   * @param {integer} width
   * @param {integer} length
   * @returns {integer} area of the rectangle
   */


const areaOfRectangle = (width, length) => {
    const area = width * length;
    return `The area of the rectangle is equal to ${area}`;
};

//Run the method
areaOfRectangle(10, 40);

